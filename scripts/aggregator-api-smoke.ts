function writeJson(filePath: string, value: unknown): void {
  writeFileSync(filePath, `${JSON.stringify(value, null, 2)}\n`, "utf8");
}

function assertCase(
  label: string,
  condition: boolean,
  details?: Record<string, unknown>
): void {
  if (!condition) {
    throw new Error(`${label} failed${details ? `: ${JSON.stringify(details)}` : ""}`);
  }
  console.log(`PASS ${label}`);
}

function sleep(ms: number): Promise<void> {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

async function requestJson<T>(
  method: "GET" | "POST",
  path: string,
  body?: unknown
): Promise<JsonResponse<T>> {
  const response = await fetch(`${baseUrl}${path}`, {
    method,
    headers: body === undefined ? undefined : { "Content-Type": "application/json" },
    body: body === undefined ? undefined : JSON.stringify(body)
  });
  const text = await response.text();
  let parsed: T;
  try {
    parsed = text ? (JSON.parse(text) as T) : ({} as T);
  } catch (error) {
    throw new Error(
      `${method} ${path} returned non-JSON status ${response.status}: ${text.slice(0, 200)}`,
      { cause: error }
    );
  }
  return { status: response.status, body: parsed };
}

async function waitForHealth(server: ChildProcessWithoutNullStreams): Promise<void> {
  const stderr: string[] = [];
  server.stderr.on("data", (chunk) => stderr.push(String(chunk)));

  for (let attempt = 0; attempt < 80; attempt += 1) {
    if (server.exitCode !== null) {
      throw new Error(`API exited early with code ${server.exitCode}: ${stderr.join("")}`);
    }

    try {
      const response = await requestJson<{ ok: boolean }>("GET", "/health");
      if (response.status === 200 && response.body.ok) {
        return;
      }
    } catch {
      // Server is still starting.
    }

    await sleep(250);
  }

  throw new Error(`Timed out waiting for ${baseUrl}/health`);
}

function stopServer(server: ChildProcessWithoutNullStreams): void {
  if (server.exitCode !== null) {
    return;
  }

  if (process.platform === "win32") {
    execFileSync("taskkill", ["/PID", String(server.pid), "/T", "/F"], {
      stdio: "ignore"
    });
    return;
  }

  server.kill("SIGTERM");
}

async function startApiServer(
  env: Record<string, string>
): Promise<ChildProcessWithoutNullStreams> {
  const tsxCli = join(
    projectRoot,
    "node_modules",
    ".pnpm",
    "tsx@4.21.0",
    "node_modules",
    "tsx",
    "dist",
    "cli.cjs"
  );
  const server = spawn(
    process.execPath,
    [tsxCli, "apps/api/src/index.ts"],
    {
      cwd: projectRoot,
      env: {
        ...process.env,
        PORT: String(port),
        ...env
      },
      stdio: ["ignore", "pipe", "pipe"]
    }
  );

  await waitForHealth(server);
  return server;
}

async function seedElection(name: string): Promise<string> {
  const electionResponse = await requestJson<{ election: { id: string } }>(
    "POST",
    "/elections",
    {
      title: `A-track API smoke ${name}`,
      description: "Generated by pnpm aggregator:api-smoke"
    }
  );
  assertCase(`${name}: create election`, electionResponse.status === 201, {
    status: electionResponse.status,
    body: electionResponse.body
  });
  const electionId = electionResponse.body.election.id;

  const candidateIds: string[] = [];
  for (const candidateName of ["Alice", "Bob", "Carol", "Dave"]) {
    const candidateResponse = await requestJson<{ candidate: { id: string } }>(
      "POST",
      `/elections/${electionId}/candidates`,
      { name: candidateName }
    );
    assertCase(`${name}: create candidate ${candidateName}`, candidateResponse.status === 201, {
      status: candidateResponse.status,
      body: candidateResponse.body
    });
    candidateIds.push(candidateResponse.body.candidate.id);
  }

  for (let index = 0; index < 8; index += 1) {
    const userResponse = await requestJson<{ userId: string }>(
      "POST",
      "/users/register",
      { name: `${name} user ${index + 1}` }
    );
    assertCase(`${name}: create user ${index + 1}`, userResponse.status === 201, {
      status: userResponse.status,
      body: userResponse.body
    });

    const voteResponse = await requestJson<{ voteId: string }>(
      "POST",
      `/elections/${electionId}/vote`,
      {
        userId: userResponse.body.userId,
        candidateId: candidateIds[index % candidateIds.length]
      }
    );
    assertCase(`${name}: cast vote ${index + 1}`, voteResponse.status === 201, {
      status: voteResponse.status,
      body: voteResponse.body
    });
  }

  return electionId;
}

function diagnosticReasons(report: AggregatorReport): string[] {
  return Array.from(
    new Set(report.invalidVoteDiagnostics.map((diagnostic) => diagnostic.reason))
  ).sort();
}

function jsonEqual(left: unknown, right: unknown): boolean {
  return JSON.stringify(left) === JSON.stringify(right);
}

function assertReport(
  name: string,
  report: AggregatorReport,
  integrityCheck: AggregatorReportIntegrityCheck,
  expectation: SmokeExpectation
): void {
  assertCase(`${name}: integrity`, integrityCheck.verified, {
    failures: integrityCheck.failures
  });
  assertCase(`${name}: totalVotes`, report.totalVotes === expectation.totalVotes, {
    actual: report.totalVotes,
    expected: expectation.totalVotes
  });
  assertCase(`${name}: validVotes`, report.validVotes === expectation.validVotes, {
    actual: report.validVotes,
    expected: expectation.validVotes
  });
  assertCase(`${name}: invalidVotes`, report.invalidVotes === expectation.invalidVotes, {
    actual: report.invalidVotes,
    expected: expectation.invalidVotes
  });
  assertCase(`${name}: duplicateVotes`, report.duplicateVotes === expectation.duplicateVotes, {
    actual: report.duplicateVotes,
    expected: expectation.duplicateVotes
  });
  assertCase(
    `${name}: receiptChainVerified`,
    report.receiptChainVerified === expectation.receiptChainVerified,
    {
      actual: report.receiptChainVerified,
      expected: expectation.receiptChainVerified
    }
  );
  assertCase(
    `${name}: proofStatus placeholder`,
    report.proofStatus === "not-generated" &&
      report.tallyProofSummary?.proofStatus === "not-generated" &&
      report.tallyProofSummary.proofId === null,
    {
      proofStatus: report.proofStatus,
      tallyProofSummary: report.tallyProofSummary
    }
  );

  const bucketVoteIds = report.partitionAudit.buckets.flatMap((bucket) => bucket.voteIds);
  const bucketVoteIdSet = new Set(bucketVoteIds);
  const diagnosticVoteIds = Array.from(
    new Set(report.invalidVoteDiagnostics.map((diagnostic) => diagnostic.voteId))
  ).sort();
  assertCase(
    `${name}: API validVoteIds match bucket ids`,
    report.validVoteIds.length === bucketVoteIds.length &&
      report.validVoteIds.every((voteId) => bucketVoteIdSet.has(voteId)),
    {
      validVoteIds: report.validVoteIds,
      bucketVoteIds
    }
  );
  assertCase(
    `${name}: API invalidVoteIds match diagnostics`,
    JSON.stringify(report.invalidVoteIds.slice().sort()) === JSON.stringify(diagnosticVoteIds),
    {
      invalidVoteIds: report.invalidVoteIds,
      diagnosticVoteIds
    }
  );
  assertCase(
    `${name}: API invalid ids excluded from buckets`,
    report.invalidVoteIds.every((voteId) => !bucketVoteIdSet.has(voteId)),
    {
      invalidVoteIds: report.invalidVoteIds,
      bucketVoteIds
    }
  );

  const reasons = diagnosticReasons(report);
  for (const reason of expectation.reasons) {
    assertCase(`${name}: reason ${reason}`, reasons.includes(reason), { reasons });
  }
}

async function runAggregatorCase(
  name: string,
  attackEndpoint: string | null,
  expectation: SmokeExpectation
): Promise<SmokeCaseResult> {
  const electionId = await seedElection(name);
  let attackStatus = 0;

  if (attackEndpoint) {
    const attackResponse = await requestJson<{ ok: boolean }>(
      "POST",
      `/attack/elections/${electionId}/${attackEndpoint}`
    );
    attackStatus = attackResponse.status;
    assertCase(`${name}: attack endpoint ${attackEndpoint}`, attackResponse.status === 200, {
      status: attackResponse.status,
      body: attackResponse.body
    });
  }

  const runResponse = await requestJson<{
    report: AggregatorReport;
    integrityCheck: AggregatorReportIntegrityCheck;
  }>("POST", `/aggregator/elections/${electionId}/run`);
  assertCase(`${name}: run aggregator`, runResponse.status === 201, {
    status: runResponse.status,
    body: runResponse.body
  });
  assertReport(name, runResponse.body.report, runResponse.body.integrityCheck, expectation);

  const reportResponse = await requestJson<{
    report: AggregatorReport;
    integrityCheck: AggregatorReportIntegrityCheck;
  }>("GET", `/aggregator/elections/${electionId}/report`);
  assertCase(`${name}: get aggregator report`, reportResponse.status === 200, {
    status: reportResponse.status,
    body: reportResponse.body
  });
  assertReport(
    `${name} report endpoint`,
    reportResponse.body.report,
    reportResponse.body.integrityCheck,
    expectation
  );
  const apiReportFile = `api_aggregator_report.${name}.json`;
  const apiAttackAliasFile = attackEndpoint
    ? `api_aggregator_report.attack-${name}.json`
    : null;
  const apiReportEvidence = {
    schemaVersion: "verivote.aggregator-api-report-evidence.v1",
    generatedBy: "pnpm aggregator:api-smoke",
    name,
    runEndpoint: `/aggregator/elections/${electionId}/run`,
    reportEndpoint: `/aggregator/elections/${electionId}/report`,
    attackEndpoint: attackEndpoint
      ? `/attack/elections/${electionId}/${attackEndpoint}`
      : null,
    runStatus: runResponse.status,
    reportStatus: reportResponse.status,
    attackStatus: attackEndpoint ? attackStatus : null,
    report: runResponse.body.report,
    integrityCheck: runResponse.body.integrityCheck
  };
  writeJson(join(reportDir, apiReportFile), apiReportEvidence);
  if (apiAttackAliasFile) {
    writeJson(join(reportDir, apiAttackAliasFile), apiReportEvidence);
  }

  return {
    name,
    endpoint: attackEndpoint ? `/attack/elections/:id/${attackEndpoint}` : "/aggregator/elections/:id/run",
    status: attackEndpoint ? attackStatus : runResponse.status,
    apiReportFile: `docs/evaluation/aggregator_reports/${apiReportFile}`,
    apiAttackAliasFile: apiAttackAliasFile
      ? `docs/evaluation/aggregator_reports/${apiAttackAliasFile}`
      : null,
    integrityVerified: runResponse.body.integrityCheck.verified,
    failures: runResponse.body.integrityCheck.failures,
    reportSummary: {
      totalVotes: runResponse.body.report.totalVotes,
      validVotes: runResponse.body.report.validVotes,
      invalidVotes: runResponse.body.report.invalidVotes,
      duplicateVotes: runResponse.body.report.duplicateVotes,
      proofStatus: runResponse.body.report.proofStatus,
      receiptChainVerified: runResponse.body.report.receiptChainVerified,
      reasons: diagnosticReasons(runResponse.body.report),
      validVoteIds: runResponse.body.report.validVoteIds,
      invalidVoteIds: runResponse.body.report.invalidVoteIds,
      partitionHash: runResponse.body.report.partitionHash,
      diagnosticsHash: runResponse.body.report.diagnosticsHash,
      auditHash: runResponse.body.report.auditHash
    }
  };
}

