async function runReportTamperSmoke(): Promise<ReportTamperSmokeResult> {
  const electionId = await seedElection("report-tamper");
  const runResponse = await requestJson<{
    report: AggregatorReport;
    integrityCheck: AggregatorReportIntegrityCheck;
  }>("POST", `/aggregator/elections/${electionId}/run`);
  assertCase("report-tamper: initial run", runResponse.status === 201, {
    status: runResponse.status,
    body: runResponse.body
  });
  assertCase("report-tamper: initial integrity", runResponse.body.integrityCheck.verified, {
    failures: runResponse.body.integrityCheck.failures
  });

  const attackResponse = await requestJson<{ ok: boolean }>(
    "POST",
    `/attack/elections/${electionId}/tamper-tally`
  );
  assertCase("report-tamper: attack endpoint tamper-tally", attackResponse.status === 200, {
    status: attackResponse.status,
    body: attackResponse.body
  });

  const reportResponse = await requestJson<{
    report: AggregatorReport;
    integrityCheck: AggregatorReportIntegrityCheck;
    tallyConsistent: boolean;
    consistencyMessage: string;
  }>("GET", `/aggregator/elections/${electionId}/report`);
  assertCase("report-tamper: get tampered report", reportResponse.status === 200, {
    status: reportResponse.status,
    body: reportResponse.body
  });
  assertCase(
    "report-tamper: integrity rejects recomputed auditHash tally tamper",
    reportResponse.body.integrityCheck.verified === false &&
      reportResponse.body.integrityCheck.checks.bucketTallyMatches === false &&
      reportResponse.body.integrityCheck.checks.tallyTotalMatchesValidVotes === false &&
      reportResponse.body.integrityCheck.checks.tallySumMatchesValidVotes === false,
    {
      integrityCheck: reportResponse.body.integrityCheck
    }
  );
  assertCase(
    "report-tamper: tally consistency detects saved report drift",
    reportResponse.body.tallyConsistent === false,
    {
      tallyConsistent: reportResponse.body.tallyConsistent,
      consistencyMessage: reportResponse.body.consistencyMessage
    }
  );

  const file = "api_aggregator_report.attack-tamper-tally.json";
  writeJson(join(reportDir, file), {
    schemaVersion: "verivote.aggregator-api-report-tamper-evidence.v1",
    generatedBy: "pnpm aggregator:api-smoke",
    name: "tamper-tally",
    endpoint: `/attack/elections/${electionId}/tamper-tally`,
    reportEndpoint: `/aggregator/elections/${electionId}/report`,
    attackStatus: attackResponse.status,
    reportStatus: reportResponse.status,
    report: reportResponse.body.report,
    integrityCheck: reportResponse.body.integrityCheck,
    tallyConsistent: reportResponse.body.tallyConsistent,
    consistencyMessage: reportResponse.body.consistencyMessage
  });

  return {
    name: "tamper-tally",
    endpoint: "/attack/elections/:id/tamper-tally",
    status: attackResponse.status,
    reportStatus: reportResponse.status,
    integrityVerified: reportResponse.body.integrityCheck.verified,
    expectedIntegrityFailures: [
      "bucketTallyMatches",
      "tallyTotalMatchesValidVotes",
      "tallySumMatchesValidVotes"
    ],
    observedFailures: reportResponse.body.integrityCheck.failures,
    tallyConsistent: reportResponse.body.tallyConsistent,
    consistencyMessage: reportResponse.body.consistencyMessage,
    auditHash: reportResponse.body.report.auditHash,
    file: `docs/evaluation/aggregator_reports/${file}`
  };
}

async function runPersistenceSmoke(): Promise<Record<string, unknown>> {
  mkdirSync(tempDir, { recursive: true });
  const sqlitePath = join(tempDir, `aggregator-api-smoke-${Date.now()}.db`);
  let firstServer: ChildProcessWithoutNullStreams | null = null;
  let secondServer: ChildProcessWithoutNullStreams | null = null;

  try {
    firstServer = await startApiServer({
      VERIVOTE_PERSISTENCE: "sqlite",
      VERIVOTE_SQLITE_PATH: sqlitePath
    });
    const electionId = await seedElection("persistence");
    const runResponse = await requestJson<{
      report: AggregatorReport;
      integrityCheck: AggregatorReportIntegrityCheck;
    }>("POST", `/aggregator/elections/${electionId}/run`);
    assertCase("persistence: initial run", runResponse.status === 201, {
      status: runResponse.status,
      body: runResponse.body
    });
    assertCase("persistence: initial integrity", runResponse.body.integrityCheck.verified, {
      failures: runResponse.body.integrityCheck.failures
    });
    const originalReport = runResponse.body.report;
    stopServer(firstServer);
    firstServer = null;
    await sleep(500);

    secondServer = await startApiServer({
      VERIVOTE_PERSISTENCE: "sqlite",
      VERIVOTE_SQLITE_PATH: sqlitePath
    });
    const loadedResponse = await requestJson<{
      report: AggregatorReport;
      integrityCheck: AggregatorReportIntegrityCheck;
    }>("GET", `/aggregator/elections/${electionId}/report`);
    assertCase("persistence: report survives restart", loadedResponse.status === 200, {
      status: loadedResponse.status,
      body: loadedResponse.body
    });
    assertCase("persistence: loaded integrity", loadedResponse.body.integrityCheck.verified, {
      failures: loadedResponse.body.integrityCheck.failures
    });
    assertCase(
      "persistence: v2 fields survive restart",
      loadedResponse.body.report.auditHash === originalReport.auditHash &&
        loadedResponse.body.report.validVoteIds.length ===
          originalReport.validVoteIds.length &&
        loadedResponse.body.report.invalidVoteIds.length ===
          originalReport.invalidVoteIds.length &&
        loadedResponse.body.report.proofStatus === originalReport.proofStatus &&
        loadedResponse.body.report.tallyProofSummary?.proofStatus ===
          originalReport.tallyProofSummary?.proofStatus &&
        loadedResponse.body.report.partitionAudit.buckets.every(
          (bucket) => Array.isArray(bucket.tokenHashes)
        ),
      {
        originalAuditHash: originalReport.auditHash,
        loadedAuditHash: loadedResponse.body.report.auditHash,
        loadedValidVoteIds: loadedResponse.body.report.validVoteIds,
        loadedInvalidVoteIds: loadedResponse.body.report.invalidVoteIds
      }
    );
    const persistentFieldNames: Array<keyof AggregatorReport> = [
      "electionId",
      "totalVotes",
      "validVotes",
      "validVoteIds",
      "invalidVotes",
      "invalidVoteIds",
      "duplicateVotes",
      "proofStatus",
      "tallyProofSummary",
      "receiptChainVerified",
      "receiptChainBreaks",
      "voteTokenHashes",
      "duplicateTokenHashes",
      "tallyResult",
      "commitmentRoot",
      "receiptRoot",
      "partitionAudit",
      "partitionHash",
      "invalidVoteDiagnostics",
      "diagnosticsHash",
      "publicInputHints",
      "pedersenAggregateAudit",
      "pedersenAggregateStatus",
      "pedersenAggregateHash",
      "pedersenTallyVerified",
      "pedersenTallyMessage",
      "pedersenContextHash",
      "auditHash",
      "createdAt"
    ];
    const fieldRoundTrip = persistentFieldNames.map((field) => ({
      field,
      equal: jsonEqual(loadedResponse.body.report[field], originalReport[field])
    }));
    assertCase(
      "persistence: all AggregatorReport v2 fields round-trip",
      fieldRoundTrip.every((item) => item.equal),
      { fieldRoundTrip }
    );

    return {
      sqlitePath,
      electionId,
      auditHash: loadedResponse.body.report.auditHash,
      integrityVerified: loadedResponse.body.integrityCheck.verified,
      fieldRoundTrip,
      validVoteIds: loadedResponse.body.report.validVoteIds,
      invalidVoteIds: loadedResponse.body.report.invalidVoteIds,
      proofStatus: loadedResponse.body.report.proofStatus,
      bucketTokenHashCounts: loadedResponse.body.report.partitionAudit.buckets.map(
        (bucket) => ({
          candidateId: bucket.candidateId,
          tokenHashCount: bucket.tokenHashes.length
        })
      )
    };
  } finally {
    if (firstServer) {
      stopServer(firstServer);
    }
    if (secondServer) {
      stopServer(secondServer);
    }
  }
}

async function main(): Promise<void> {
  mkdirSync(reportDir, { recursive: true });

  let server: ChildProcessWithoutNullStreams | null = null;

  try {
    server = await startApiServer({
      VERIVOTE_PERSISTENCE: "memory"
    });

    const cases = [
      await runAggregatorCase("normal", null, {
        totalVotes: 8,
        validVotes: 8,
        invalidVotes: 0,
        duplicateVotes: 0,
        receiptChainVerified: true,
        reasons: []
      }),
      await runAggregatorCase("duplicate-token", "inject-duplicate-vote", {
        totalVotes: 9,
        validVotes: 8,
        invalidVotes: 1,
        duplicateVotes: 1,
        receiptChainVerified: true,
        reasons: ["duplicate-token"]
      }),
      await runAggregatorCase("invalid-candidate", "inject-invalid-vote", {
        totalVotes: 9,
        validVotes: 8,
        invalidVotes: 1,
        duplicateVotes: 0,
        receiptChainVerified: true,
        reasons: ["invalid-candidate"]
      }),
      await runAggregatorCase("non-one-hot", "inject-non-one-hot-vote", {
        totalVotes: 9,
        validVotes: 8,
        invalidVotes: 1,
        duplicateVotes: 0,
        receiptChainVerified: true,
        reasons: ["invalid-one-hot"]
      }),
      await runAggregatorCase(
        "candidate-vector-mismatch",
        "inject-candidate-vector-mismatch",
        {
          totalVotes: 9,
          validVotes: 8,
          invalidVotes: 1,
          duplicateVotes: 0,
          receiptChainVerified: true,
          reasons: ["candidate-vector-mismatch"]
        }
      ),
      await runAggregatorCase("commitment-tamper", "tamper-commitment", {
        totalVotes: 8,
        validVotes: 7,
        invalidVotes: 1,
        duplicateVotes: 0,
        receiptChainVerified: false,
        reasons: ["commitment-opening-failed", "receipt-chain-break"]
      }),
      await runAggregatorCase("receipt-chain-delete", "delete-vote", {
        totalVotes: 7,
        validVotes: 0,
        invalidVotes: 7,
        duplicateVotes: 0,
        receiptChainVerified: false,
        reasons: ["receipt-chain-break"]
      })
    ];

    const exportSmoke = await runExportSmoke();
    const reportTamperSmoke = await runReportTamperSmoke();
    stopServer(server);
    server = null;

    const persistenceSmoke = await runPersistenceSmoke();
    const outputFile = join(reportDir, "api_smoke.json");

    writeJson(outputFile, {
      schemaVersion: "verivote.aggregator-api-smoke.v1",
      generatedBy: "pnpm aggregator:api-smoke",
      baseUrl,
      caseCount: cases.length,
      allIntegrityVerified: cases.every((item) => item.integrityVerified),
      cases,
      exportSmoke,
      reportTamperSmoke,
      persistenceSmoke
    });

    console.log(`Wrote API smoke evidence to ${join("docs", "evaluation", "aggregator_reports", basename(outputFile))}`);
  } finally {
    if (server) {
      stopServer(server);
    }
  }
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
